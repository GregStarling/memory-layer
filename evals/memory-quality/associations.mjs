import { createSQLiteAdapter } from '../../dist/adapters/sqlite/index.js';
import { wrapSyncAdapter } from '../../dist/adapters/sync-to-async.js';
import { autoDetectAssociations, traverseAssociations } from '../../dist/core/associations.js';
import { buildMemoryContext } from '../../dist/core/context.js';
import { assertScenario, ratio, tagEvalOutput } from './shared.mjs';

function makeScope(overrides = {}) {
  return {
    tenant_id: 'eval',
    system_id: 'memory-quality',
    workspace_id: 'associations',
    scope_id: 'thread-1',
    ...overrides,
  };
}

// ---------- Metric 1: association_auto_detection ----------
async function evalAutoDetection(adapter, asyncAdapter, scope) {
  // Seed knowledge facts with varying degrees of relatedness
  const k1 = adapter.insertKnowledgeMemory({
    ...scope,
    fact: 'The staging API runs on port 8080 behind nginx.',
    fact_type: 'reference',
    knowledge_class: 'project_fact',
    source: 'user_stated',
    confidence: 'high',
  });

  const k2 = adapter.insertKnowledgeMemory({
    ...scope,
    fact: 'The staging API is deployed using nginx as a reverse proxy on port 8080.',
    fact_type: 'reference',
    knowledge_class: 'project_fact',
    source: 'user_stated',
    confidence: 'high',
  });

  const k3 = adapter.insertKnowledgeMemory({
    ...scope,
    fact: 'User prefers dark mode in the IDE.',
    fact_type: 'preference',
    knowledge_class: 'preference',
    source: 'user_stated',
    confidence: 'high',
  });

  const k4 = adapter.insertKnowledgeMemory({
    ...scope,
    fact: 'The production API runs on port 443 behind a load balancer.',
    fact_type: 'reference',
    knowledge_class: 'project_fact',
    source: 'user_stated',
    confidence: 'high',
  });

  // Auto-detect associations for k2 against existing knowledge
  const allKnowledge = adapter.getActiveKnowledgeMemory(scope);
  const k2Assocs = await autoDetectAssociations(asyncAdapter, scope, k2, allKnowledge);

  // k2 should be associated with k1 (high similarity — both about staging API nginx port 8080)
  const foundK1Link = k2Assocs.some(
    (a) =>
      (a.source_id === k1.id || a.target_id === k1.id) &&
      (a.association_type === 'supports' || a.association_type === 'related_to'),
  );

  // k2 should have some relation with k4 (moderate — both about APIs and ports)
  const k4Assocs = await autoDetectAssociations(asyncAdapter, scope, k4, allKnowledge);
  const foundK4Link = k4Assocs.some(
    (a) =>
      (a.source_id === k1.id || a.target_id === k1.id || a.source_id === k2.id || a.target_id === k2.id),
  );

  // k3 (dark mode preference) should NOT be associated with k1/k2 (API infrastructure)
  const k3Assocs = await autoDetectAssociations(asyncAdapter, scope, k3, allKnowledge);
  const noSpuriousK3Link = !k3Assocs.some(
    (a) =>
      (a.source_id === k1.id || a.target_id === k1.id || a.source_id === k2.id || a.target_id === k2.id),
  );

  // All auto-generated associations should have auto_generated=true
  const allAssocs = [...k2Assocs, ...k3Assocs, ...k4Assocs];
  const allAutoGenerated = allAssocs.length === 0 || allAssocs.every((a) => a.auto_generated === true);

  const checks = [foundK1Link, noSpuriousK3Link, allAutoGenerated];
  const score = ratio(checks.filter(Boolean).length, checks.length);

  return {
    metric: score,
    passed: score >= 0.9,
    k2AssocCount: k2Assocs.length,
    k3AssocCount: k3Assocs.length,
    k4AssocCount: k4Assocs.length,
    foundK1Link,
    foundK4Link,
    noSpuriousK3Link,
  };
}

// ---------- Metric 2: association_traversal_bounds ----------
async function evalTraversalBounds(adapter, asyncAdapter, scope) {
  // Insert a chain of associations: k1 -> k2 -> k5 -> k6
  const k5 = adapter.insertKnowledgeMemory({
    ...scope,
    fact: 'The nginx config is at /etc/nginx/sites-available/api.',
    fact_type: 'reference',
    knowledge_class: 'project_fact',
    source: 'user_stated',
    confidence: 'high',
  });

  const k6 = adapter.insertKnowledgeMemory({
    ...scope,
    fact: 'SSL certificates are renewed automatically via certbot.',
    fact_type: 'reference',
    knowledge_class: 'project_fact',
    source: 'user_stated',
    confidence: 'high',
  });

  // Manually create chain: existing k1 -> k5 -> k6
  const allKnowledge = adapter.getActiveKnowledgeMemory(scope);
  const k1 = allKnowledge.find((k) => k.fact.includes('staging API runs'));

  await asyncAdapter.insertAssociation({
    ...scope,
    source_kind: 'knowledge',
    source_id: k1.id,
    target_kind: 'knowledge',
    target_id: k5.id,
    association_type: 'related_to',
    confidence: 0.6,
  });

  await asyncAdapter.insertAssociation({
    ...scope,
    source_kind: 'knowledge',
    source_id: k5.id,
    target_kind: 'knowledge',
    target_id: k6.id,
    association_type: 'related_to',
    confidence: 0.5,
  });

  // Traverse with depth=1 — should reach k5 but not k6
  const depth1 = await traverseAssociations(asyncAdapter, scope, 'knowledge', k1.id, {
    maxDepth: 1,
    maxNodes: 20,
  });
  const depth1HasK5 = depth1.nodes.some((n) => n.id === k5.id);
  const depth1NoK6 = !depth1.nodes.some((n) => n.id === k6.id);

  // Traverse with depth=2 — should reach both k5 and k6
  const depth2 = await traverseAssociations(asyncAdapter, scope, 'knowledge', k1.id, {
    maxDepth: 2,
    maxNodes: 20,
  });
  const depth2HasK5 = depth2.nodes.some((n) => n.id === k5.id);
  const depth2HasK6 = depth2.nodes.some((n) => n.id === k6.id);

  // Traverse with maxNodes=2 — should limit total nodes
  const limited = await traverseAssociations(asyncAdapter, scope, 'knowledge', k1.id, {
    maxDepth: 10,
    maxNodes: 2,
  });
  const limitedNodeCount = limited.nodes.length <= 2;

  const checks = [depth1HasK5, depth1NoK6, depth2HasK5, depth2HasK6, limitedNodeCount];
  const score = ratio(checks.filter(Boolean).length, checks.length);

  return {
    metric: score,
    passed: score >= 0.9,
    depth1Nodes: depth1.nodes.length,
    depth2Nodes: depth2.nodes.length,
    limitedNodes: limited.nodes.length,
  };
}

// ---------- Metric 3: association_retrieval_boost ----------
async function evalRetrievalBoost(adapter, asyncAdapter, scope) {
  // Seed a turn so context builder has something to work with
  adapter.insertTurn({
    ...scope,
    session_id: 'assoc-session',
    actor: 'user',
    role: 'user',
    content: 'Tell me about the staging API.',
    token_estimate: 10,
    created_at: Math.floor(Date.now() / 1000) - 100,
  });

  // Build context with relevance query — should pick up associatedKnowledge
  const context = await buildMemoryContext(asyncAdapter, scope, {
    sessionId: 'assoc-session',
    relevanceQuery: 'staging API nginx',
    associationMinConfidence: 0.2,
  });

  // associatedKnowledge should exist as a field (backwards compatible)
  const hasField = Array.isArray(context.associatedKnowledge);

  // If relevant knowledge was found, associated should be populated
  // (depends on whether the search found the staging facts)
  const relevantFound = context.relevantKnowledge.length > 0;
  const associatedPopulated = relevantFound
    ? context.associatedKnowledge.length >= 0 // may be 0 if all associations already in relevantKnowledge
    : true;

  // Associated knowledge should not duplicate items already in relevantKnowledge
  const relevantIds = new Set(context.relevantKnowledge.map((k) => k.id));
  const noDuplicates = context.associatedKnowledge.every((k) => !relevantIds.has(k.id));

  // All associated items should have a non-retired knowledge state
  const allActive = context.associatedKnowledge.every(
    (k) => k.knowledge_state !== 'retired',
  );

  const checks = [hasField, associatedPopulated, noDuplicates, allActive];
  const score = ratio(checks.filter(Boolean).length, checks.length);

  return {
    metric: score,
    passed: score >= 0.9,
    relevantCount: context.relevantKnowledge.length,
    associatedCount: context.associatedKnowledge.length,
    noDuplicates,
  };
}

export async function runAssociationEvals(_options = {}) {
  const scope = makeScope();
  const adapter = createSQLiteAdapter(':memory:');
  const asyncAdapter = wrapSyncAdapter(adapter);

  try {
    const detection = await evalAutoDetection(adapter, asyncAdapter, scope);
    const traversal = await evalTraversalBounds(adapter, asyncAdapter, scope);
    const boost = await evalRetrievalBoost(adapter, asyncAdapter, scope);

    const metrics = {
      associationAutoDetection: detection.metric,
      associationTraversalBounds: traversal.metric,
      associationRetrievalBoost: boost.metric,
    };

    return tagEvalOutput('associations', {
      metrics,
      scenarios: [
        assertScenario('auto_detect_creates_relevant_associations', detection.passed, {
          k2AssocCount: detection.k2AssocCount,
          foundK1Link: detection.foundK1Link,
          noSpuriousK3Link: detection.noSpuriousK3Link,
        }),
        assertScenario('traversal_respects_depth_and_node_limits', traversal.passed, {
          depth1Nodes: traversal.depth1Nodes,
          depth2Nodes: traversal.depth2Nodes,
          limitedNodes: traversal.limitedNodes,
        }),
        assertScenario('context_builder_includes_associated_knowledge', boost.passed, {
          relevantCount: boost.relevantCount,
          associatedCount: boost.associatedCount,
          noDuplicates: boost.noDuplicates,
        }),
      ],
      diagnostic: {
        metricTraces: {
          associationAutoDetection: {
            stage: 'auto_detect',
            k2AssocCount: detection.k2AssocCount,
            k3AssocCount: detection.k3AssocCount,
          },
          associationTraversalBounds: {
            stage: 'traverse',
            depth1Nodes: traversal.depth1Nodes,
            depth2Nodes: traversal.depth2Nodes,
          },
          associationRetrievalBoost: {
            stage: 'context_build',
            associatedCount: boost.associatedCount,
          },
        },
      },
    });
  } finally {
    adapter.close();
  }
}
